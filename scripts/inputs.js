function updateStreetsList(cityKey) {
  streetsList.innerHTML = "";

  if (streetsDatabase[cityKey]) {
    streetsDatabase[cityKey].forEach((street) => {
      const option = document.createElement("option");
      option.value = street;
      streetsList.appendChild(option);
    });
    console.log(
      `–ó–∞–≥—Ä—É–∂–µ–Ω–æ ${streetsDatabase[cityKey].length} —É–ª–∏—Ü –¥–ª—è –≥–æ—Ä–æ–¥–∞ ${cityKey}`
    );
  } else {
    console.log("–ì–æ—Ä–æ–¥ –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –±–∞–∑–µ:", cityKey);
  }
}

